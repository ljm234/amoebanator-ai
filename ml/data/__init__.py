"""
Data Acquisition and Management Module.

This package provides comprehensive infrastructure for secure medical data
ingestion, validation, versioning, and provenance tracking. Designed for
clinical diagnostic system data pipelines with HIPAA-compliant handling.

Submodules
----------
acquisition : Data transfer and ingestion protocols
clinical : Clinical record parsing and validation
microscopy : Microscopy image loading and preprocessing
versioning : Dataset versioning and lineage tracking
literature : PubMed literature mining and figure extraction
synthetic : Synthetic data generation with diffusion models
who_database : WHO Global Health Observatory data access
pathology_atlas : Licensed pathology atlas access
negative_collection : Negative class sample collection
labeling : Label Studio integration for annotation
annotation_protocol : Expert annotation workflow management
quality_assurance : Data quality validation pipeline
dvc_versioning : DVC-based dataset version control
"""

